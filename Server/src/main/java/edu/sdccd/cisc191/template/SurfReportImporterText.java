package edu.sdccd.cisc191.template;

import java.time.LocalDateTime;
import java.util.Date;
// this class imports surf reports by copying and pasting text from the Internet website SurfLine.com
public class SurfReportImporterText implements SurfReportImporter{
    @Override //@Override is not necessary but is a good programming habit
    public SurfReport[] importSurfReport() {// this method signature is in the interface
        SurfReport[] surfReports = new SurfReport[10];
        surfReports[0] = new SurfReport(new Date(), "chest-high to a couple feet overhead");
        return surfReports;
    }
}
